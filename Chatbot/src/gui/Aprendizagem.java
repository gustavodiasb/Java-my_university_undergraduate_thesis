    public void limparCampos() {
        txtFrase.setText("");
        txtSignificado.setText("");
        cbFrase.removeAllItems();
        cbSignificado.removeAllItems();
        func.preencherSignificado(cbSignificado);
        func.listarFrase(cbFrase);
        fraseList2 = java.beans.Beans.isDesignTime() ? java.util.Collections.emptyList() : fraseQuery2.getResultList();
        significadoList = java.beans.Beans.isDesignTime() ? java.util.Collections.emptyList() : significadoQuery.getResultList();
    }    
    
    private void inserirSignificadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirSignificadoActionPerformed
        if (txtSignificado.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Vazio", "Vazio", JOptionPane.WARNING_MESSAGE);
        } else {
            func.inserirSignificado(txtSignificado.getText());
            JOptionPane.showMessageDialog(null, "Inserido com sucesso", "Sucesso", JOptionPane.PLAIN_MESSAGE);
            limparCampos();
        }
    }//GEN-LAST:event_inserirSignificadoActionPerformed

    private void inserirFraseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirFraseActionPerformed
        if (txtFrase.getText().equals("")) {
            JOptionPane.showConfirmDialog(null, "Campo Vazio", "Vazio", JOptionPane.WARNING_MESSAGE);
        } else {
            func.inserirFrase(txtFrase.getText(), cbSignificado.getSelectedItem().toString());
            JOptionPane.showMessageDialog(null, "Inserido com sucesso", "Sucesso", JOptionPane.PLAIN_MESSAGE);
            limparCampos();
        }
    }//GEN-LAST:event_inserirFraseActionPerformed

    private void inserirRespostaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirRespostaActionPerformed
        func.inserirResposta(cbFrase.getSelectedItem().toString());
        JOptionPane.showMessageDialog(null, "Inserido com sucesso", "Sucesso", JOptionPane.PLAIN_MESSAGE);
        limparCampos();
    }//GEN-LAST:event_inserirRespostaActionPerformed

