    private void eventoTabela() {
        if (tbFuncionario.getSelectedRow() != -1) {
            
            txtId.setText(tbFuncionario.getValueAt(tbFuncionario.getSelectedRow(), 0).toString());
            txtNome.setText(tbFuncionario.getValueAt(tbFuncionario.getSelectedRow(), 1).toString());
            txtCargo.setText(tbFuncionario.getValueAt(tbFuncionario.getSelectedRow(), 2).toString());
            
            int id = (int) tbFuncionario.getValueAt(tbFuncionario.getSelectedRow(), 0);
            Connection connection = ConexaoDB.getConnection();
            PreparedStatement stmt = null;
            ResultSet rs = null;
            try {
            stmt = connection.prepareStatement("SELECT email_funcionario, login_funcionario, senha_funcionario, "
                    + "acesso_funcionario FROM tb_funcionario WHERE id_funcionario ='" + id + "'");
            rs = stmt.executeQuery();
            rs.first();
            txtEmail.setText(rs.getString(1));
            txtLogin.setText(rs.getString(2));
            txtSenha.setText(rs.getString(3));
            cbAcesso.addItem(rs.getString(4));
            cbAcesso.setSelectedItem(rs.getString(4));
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro ao preencher formul치rio... " + e);
            } finally {
                ConexaoDB.closeConnection(connection, stmt, rs);
            }
        }
    }
    
    
    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tbFuncionario.getModel();
        modelo.setNumRows(0);
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();        
        
        for (Funcionario funcionario : funcionarioDAO.read()) {            
            modelo.addRow(new Object[]{
                funcionario.getId(),
                funcionario.getNome(),
                funcionario.getCargo(),
            });
        }
    }
    
    public void readJTableForDesk(String desc) {
        DefaultTableModel modelo = (DefaultTableModel) tbFuncionario.getModel();
        modelo.setNumRows(0);
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();        
        
        for (Funcionario funcionario : funcionarioDAO.readForDesc(desc)) {
            
            modelo.addRow(new Object[]{
                funcionario.getId(),
                funcionario.getNome(),
                funcionario.getCargo()
            });
        }
    }
    
    
