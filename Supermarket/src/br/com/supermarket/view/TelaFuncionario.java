    public void validaCampos() {
        if (txtNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o Nome", "Aten칞칚o!", JOptionPane.WARNING_MESSAGE);
            txtNome.requestFocus();
        } else if (!isEmailValid(txtEmail.getText())) {
            JOptionPane.showMessageDialog(null, "Informe um e-mail v치lido", "Aten칞칚o!", JOptionPane.WARNING_MESSAGE);
            txtEmail.requestFocus();
        } else if (txtLogin.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o Login", "Aten칞칚o!", JOptionPane.WARNING_MESSAGE);
            txtLogin.requestFocus();
        } else if (new String (txtSenha.getPassword()).trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o Senha", "Aten칞칚o!", JOptionPane.WARNING_MESSAGE);
            txtSenha.requestFocus();
        } else if (txtCargo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o Cargo", "Aten칞칚o!", JOptionPane.WARNING_MESSAGE);
            txtCargo.requestFocus();
        } else {
            //FuncionarioController funcionarioController = new FuncionarioController();
            Funcionario funcionario = new Funcionario();
            FuncionarioDAO funcionarioDAO = new FuncionarioDAO();

            funcionario.setNome(txtNome.getText());
            funcionario.setEmail(txtEmail.getText());
            funcionario.setCargo(txtCargo.getText());
            funcionario.setLogin(txtLogin.getText());
            funcionario.setSenha(new String (txtSenha.getPassword()));
            /*funcionario.setSexo(cbSexo.getSelectedItem().toString());*/
            funcionario.setAcesso(cbAcesso.getSelectedItem().toString());

            if (txtId.getText().equals("")) {
                funcionarioDAO.create(funcionario);
            } else {
                funcionario.setId((int)tbFuncionario.getValueAt(tbFuncionario.getSelectedRow(), 0));
                funcionarioDAO.update(funcionario);
            }
            readJTable();
            limparCampos();
        }
    }
    
    /* VALIDAR EMAIL */
    public static boolean isEmailValid(String email) {
        if ((email == null) || (email.trim().length() == 0))
            return false;

            String emailPattern = "\\b(^[_A-Za-z0-9-]+(\\.[_A-Za-z0-9-]+)*@([A-Za-z0-9-])+(\\.[A-Za-z0-9-]+)*((\\.[A-Za-z0-9]{2,})|(\\.[A-Za-z0-9]{2,}\\.[A-Za-z0-9]{2,}))$)\\b";
            Pattern pattern = Pattern.compile(emailPattern, Pattern.CASE_INSENSITIVE);
            Matcher matcher = pattern.matcher(email);
            return matcher.matches();
        }
