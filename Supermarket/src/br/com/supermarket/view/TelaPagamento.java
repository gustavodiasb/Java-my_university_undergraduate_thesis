    private void debitoCredito() {
        limparCampos();
        double troco = 0;
        txtTotalPago.setEditable(false); 
        txtTotalPago.setText(String.valueOf(venda_concluida.getValorVenda()));
        troco = (venda_concluida.getValorVenda() - venda_concluida.getValorVenda());
        txtTroco.setText(String.valueOf(String.format("%.2f", troco)));
        validaPagamento = true;
        btConcluirCompra.setEnabled(true);
    }
    
    private void dinheiro() {
        btConcluirCompra.setEnabled(true);
        if (txtTotalPago.getText().trim().isEmpty()) {
            txtTroco.setText("");
        } else {
            double troco = 0, totalPago = Double.valueOf(txtTotalPago.getText().toString().replace(",", "."));
            if (totalPago >= 0) {
                troco = totalPago - venda_concluida.getValorVenda();
                txtTroco.setText(String.valueOf(String.format("%.2f", troco)));
                if (totalPago >= venda_concluida.getValorVenda()) {
                    validaPagamento = true;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Informe valores positivos", "Valor Inválido", JOptionPane.ERROR_MESSAGE);
            txtTotalPago.setText("");
            }
        }
    }
        
