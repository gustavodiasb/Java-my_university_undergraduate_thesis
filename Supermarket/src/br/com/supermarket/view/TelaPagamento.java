    private void btConcluirCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConcluirCompraActionPerformed
        if (validaPagamento) {
            VendaProdutoDAO vendaProdutoDAO = new VendaProdutoDAO();
            vendaProdutoDAO.iniciaVenda(venda_concluida);
            imprimirNota();           
        } else {
            JOptionPane.showMessageDialog(null, "Valor insuficiente para concluir a compra", 
                    "Valor Insuficiente", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btConcluirCompraActionPerformed

    
    /* IMPRIME */
    public void imprimirNota() {
        venda_concluida.setConteudo("\n\n\nTotal: " + venda_concluida.getValorVenda() + 
                "\n\nData: " + venda_concluida.getDataVenda() + "\n\n\n           Volte Sempre!!!       ");
        Document document = new Document();
        String nomeDoc = ("venda.pdf");
        
        try {
            PdfWriter.getInstance(document, new FileOutputStream(nomeDoc));            
            document.open();
            document.add(new Phrase(venda_concluida.getConteudo()));
            TelaVenda telaVenda = new TelaVenda();
            telaVenda.limparCampos();
            this.dispose();
        } catch (DocumentException | FileNotFoundException ex) {
            System.out.println("Erro: " + ex);
        } finally {
            document.close();
        }
        
        try {
            Desktop.getDesktop().open(new File(nomeDoc));
        } catch (IOException ex) {
            System.out.println("Erro: " + ex);
        }
    }    
