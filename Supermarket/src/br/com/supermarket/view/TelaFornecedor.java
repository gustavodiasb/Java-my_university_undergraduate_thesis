    private void validaCampos() {
        if (txtNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o Nome", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtNome.requestFocus();
        } else {
            Fornecedor fornecedor = new Fornecedor();
            FornecedorDAO fornecedorDAO = new FornecedorDAO();

            fornecedor.setNome(txtNome.getText());
            fornecedor.setDescricao(taDescricao.getText());
            
            if (txtId.getText().equals("")) {
                fornecedorDAO.create(fornecedor);
            } else {
                fornecedor.setId((int)tbFornecedor.getValueAt(tbFornecedor.getSelectedRow(), 0));
                fornecedorDAO.update(fornecedor);
            }
            readJTable();
            limparCampos();
        }
    }
    
    private void eventoTabela() {
        if (tbFornecedor.getSelectedRow() != -1) {
            Fornecedor fornecedor = new Fornecedor();
            
            txtId.setText(tbFornecedor.getValueAt(tbFornecedor.getSelectedRow(), 0).toString());
            txtNome.setText(tbFornecedor.getValueAt(tbFornecedor.getSelectedRow(), 1).toString());
            
            /* BUSCAR CAMPOS NO BANCO */
            int id = (int) tbFornecedor.getValueAt(tbFornecedor.getSelectedRow(), 0);
            Connection connection = ConexaoDB.getConnection();
            PreparedStatement stmt = null;
            ResultSet rs = null;
            try {
                stmt = connection.prepareStatement("SELECT descricao_fornecedor"
                        + " FROM tb_fornecedor WHERE id_fornecedor ='" + id + "'");
                rs = stmt.executeQuery();
                rs.first();
                taDescricao.setText(rs.getString(1));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro ao preencher formulário... " + e, "Erro", JOptionPane.ERROR_MESSAGE);
            } finally {
                ConexaoDB.closeConnection(connection, stmt, rs);
            }
        }
    }
    
    private void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tbFornecedor.getModel();
        modelo.setNumRows(0);
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        
        for (Fornecedor fornecedor : fornecedorDAO.read()) {            
            modelo.addRow(new Object[]{
                fornecedor.getId(),
                fornecedor.getNome(),
            });
        }
    }
    
    private void readJTableForDesk(String desc) {
        DefaultTableModel modelo = (DefaultTableModel) tbFornecedor.getModel();
        modelo.setNumRows(0);
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        
        for (Fornecedor fornecedor : fornecedorDAO.readForDesc(desc)) {            
            modelo.addRow(new Object[]{
                fornecedor.getId(),
                fornecedor.getNome(),
            });
        }
    }
    
