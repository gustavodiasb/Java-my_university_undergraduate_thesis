    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (tbFornecedor.getSelectedRow() != -1) {

            /* Pré configura a mensagem do ConfirmDialog */
            String alertaNome = String.valueOf(tbFornecedor.getValueAt(tbFornecedor.getSelectedRow(), 1));
            String alerta = "Deseja prosseguir com a exclusão do " + alertaNome + "?";

            if (JOptionPane.showConfirmDialog(this, alerta, "ATENÇÃO!!!", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                Fornecedor fornecedor = new Fornecedor();
                FornecedorDAO fornecedorDAO = new FornecedorDAO();

                fornecedor.setId((int)tbFornecedor.getValueAt(tbFornecedor.getSelectedRow(), 0));

                fornecedorDAO.delete(fornecedor);
                readJTable();
                limparCampos();
            } else {
                System.out.println("opção 2");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione na lista abaixo", "Escolha uma opção!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btExcluirActionPerformed

