    public void filtroFuncProd(String escolha) {
        String sql = "";
        opSelect = escolha;
        if (escolha.equals("Funcionario")) {
            //System.out.println("Func");
            sql = "select id_funcionario, nome_funcionario from tb_funcionario;";
        } else if (escolha.equals("Produto")) {
            //System.out.println("Prod");
            sql = "SELECT id_produto, nome_produto FROM tb_produto";
        }
        
        Connection connection = ConexaoDB.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;
        try {
            stmt = connection.prepareStatement(sql);
            rs = stmt.executeQuery();
            
            DefaultTableModel modelo = (DefaultTableModel) tbFuncProd.getModel();
            modelo.setNumRows(0);
            
            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt(1),
                    rs.getString(2)
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher formulário... " + e);
        } finally {
            ConexaoDB.closeConnection(connection, stmt, rs);
        }
    }
    
    private void eventoTabela() {
        if (tbFuncProd.getSelectedRow() != -1) {
            tbFuncProd.getValueAt(tbFuncProd.getSelectedRow(), 0).toString();
            tbFuncProd.getValueAt(tbFuncProd.getSelectedRow(), 1).toString();
            
            /* BUSCAR CAMPOS NO BANCO */
            String texto = "";
            int id = (int) tbFuncProd.getValueAt(tbFuncProd.getSelectedRow(), 0);
            Connection connection = ConexaoDB.getConnection();
            PreparedStatement stmt = null;
            ResultSet rs = null;
            try {
                if (opSelect.equals("Funcionario")) {
                    stmt = connection.prepareStatement("SELECT * FROM view_venda WHERE id_funcionario ='" + id + "' ORDER BY data_venda ASC");
                    rs = stmt.executeQuery();
                    //rs.first();
                    while (rs.next()) {
                        texto += "ID da Venda: " + (String.valueOf(rs.getInt(1)))+"\n";
                        texto += "Valor da Venda: " + (String.valueOf(rs.getDouble(2)))+"\n";
                        texto += "Data da Venda: " + (rs.getString(3))+"\n";
                        texto += "ID do Funcionário: " + (String.valueOf(rs.getInt(4)))+" - ";
                        texto += "Nome do Funcionário: " + (rs.getString(5))+"\n\n";
                    }
                } else if (opSelect.equals("Produto")) {
                    stmt = connection.prepareStatement("SELECT DISTINCT * FROM view_itensVenda WHERE id_produto ='" + id + "' ORDER BY data_venda ASC");
                    rs = stmt.executeQuery();
                    //rs.first();
                    while (rs.next()) {
                        texto +=  "ID da Venda: " + (String.valueOf(rs.getInt(1)))+"\n";
                        texto +=  "Data da Venda: " + (rs.getString(2))+"\n";
                        texto += "ID do Produto: " + (String.valueOf(rs.getString(3)))+" - ";
                        texto += "Nome do Produto: " + (rs.getString(4))+"\n";
                        texto += "Quantidade do Produto " + (String.valueOf(rs.getInt(5)))+"\n\n";
                    }
                }
                
                taFuncProd.setText(texto);
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro ao preencher formulário... " + e, "Erro", JOptionPane.ERROR_MESSAGE);
            } finally {
                ConexaoDB.closeConnection(connection, stmt, rs);
            }
        }
    }


