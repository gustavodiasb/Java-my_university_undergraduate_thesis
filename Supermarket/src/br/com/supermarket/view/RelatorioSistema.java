    private void btReporEstoqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btReporEstoqueActionPerformed
        if (tbEstoque.getSelectedRow() != -1) {
            
            Produto produtoEstoque = new Produto();

            produtoEstoque.setId((int)tbEstoque.getValueAt(tbEstoque.getSelectedRow(), 0));
            produtoEstoque.setNome(tbEstoque.getValueAt(tbEstoque.getSelectedRow(), 1).toString());
            produtoEstoque.setQuantidade((int)tbEstoque.getValueAt(tbEstoque.getSelectedRow(), 2));
            produtoEstoque.setQuantidadeMinimaEstoque((int)tbEstoque.getValueAt(tbEstoque.getSelectedRow(), 3));

            TelaReporEstoque telaReporEstoque = new TelaReporEstoque();
            telaReporEstoque.reporEstoque(produtoEstoque);
            telaReporEstoque.setVisible(true);
            
            readJTableEstoque();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione o produto desejado", "Escolha uma opção", JOptionPane.WARNING_MESSAGE);
            tbEstoque.requestFocus();
        }
    }//GEN-LAST:event_btReporEstoqueActionPerformed

