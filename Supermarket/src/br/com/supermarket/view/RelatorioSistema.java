    public void validaTabela() {
        tbEstoque.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {

            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {
                JLabel label = (JLabel) super.getTableCellRendererComponent(table, value,
                        isSelected, hasFocus, row, column);
                
                Color c = Color.WHITE;
                Object minEstoque = tbEstoque.getValueAt(row, 3);
                Object estoque = tbEstoque.getValueAt(row, 2);
                if ((minEstoque != null && estoque != null) 
                        && Integer.parseInt(minEstoque.toString()) >= Integer.parseInt(estoque.toString())) {
                    c = Color.RED;
                }
                label.setBackground(c);
                tbEstoque.setSelectionForeground(Color.GREEN);
                    
                return label;
            }
        });
    }
    
