    private void txtCodigoBarrasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoBarrasKeyReleased
        if ((txtCodigoBarras.getText().length() < 4) && (evt.getKeyCode() == KeyEvent.VK_ENTER)) { 
            txtQuantidade.setText(txtCodigoBarras.getText());
            txtCodigoBarras.setText("");
        } else if (txtCodigoBarras.getText().length() == 13 && (evt.getKeyCode() == KeyEvent.VK_ENTER)) {
            readJTableForDesk(txtCodigoBarras.getText());
            txtCodigoBarras.setText(""); 
            txtQuantidade.setText("");
        } else if (txtCodigoBarras.getText().length() > 13) {
            JOptionPane.showMessageDialog(null, "CÃ³digo de Barras InvÃ¡lido");
            txtCodigoBarras.setText("");
        }
    }//GEN-LAST:event_txtCodigoBarrasKeyReleased

