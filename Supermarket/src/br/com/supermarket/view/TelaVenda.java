    public void readJTableForDesk(String desc) {
        DefaultTableModel modeloProduto = (DefaultTableModel) tbProdutos.getModel();
        VendaProdutoDAO vendaProdutoDAO = new VendaProdutoDAO();
        String nomeProduto, descricao = "", precoUnit = "";
        int prod_id = 0;
                
        for (Produto produto : vendaProdutoDAO.readForDesc(desc)) {
            prod_id = produto.getId();
            nomeProduto = produto.getNome();
            descricao = nomeProduto + " - " + produto.getMarca();
            precoUnit = String.valueOf(String.format("%.2f", produto.getValorVenda()));
        }
        
        if (txtQuantidade.getText().equals("")) {
            txtQuantidade.setText("1");
        }
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        
        modeloProduto.addRow(new Object[]{ prod_id, quantidade, descricao, precoUnit });
        calculaTotal();
    }
    
    private void calculaTotal() {
        total = 0;
        for (int i = 0; i <= tbProdutos.getRowCount()-1; i++) {
            qtde = Integer.parseInt(tbProdutos.getValueAt(i, 1).toString());
            prec = Double.parseDouble(tbProdutos.getValueAt(i, 3).toString().replace(",", "."));
            total += (qtde * prec);
        }
        //System.out.println("Total: " + total);
        txtTotal.setText(String.valueOf(String.format("%.2f", total)));
    }
    
    private void adicionaItem() {
        //int qtde = 0, id_prod = 0; 
        int id_prod = 0; 
        String nome_prod = "";
        
        for (int i=0; i <= tbProdutos.getRowCount()-1; i++) {
            Venda vendaLista = new Venda();
            id_prod = Integer.parseInt(tbProdutos.getValueAt(i, 0).toString());
            qtde = Integer.parseInt(tbProdutos.getValueAt(i, 1).toString());
            nome_prod = tbProdutos.getValueAt(i, 2).toString();
            prec = Double.parseDouble(tbProdutos.getValueAt(i, 3).toString().replace(",", "."));
                        
            //total += (qtde * prec);
            venda.setConteudo(id_prod + "      x      " + nome_prod + "   x   " + qtde + "   x " + prec + "\n");
            //System.out.println(tbProdutos.getValueAt(i, 0).toString() + nome_prod + tbProdutos.getValueAt(i, 1).toString() + tbProdutos.getValueAt(i, 3).toString() + "\n");
                        
            vendaLista.setId_produto(id_prod);
            vendaLista.setQtdeVendaProduto(qtde);
            
            venda.v1.add(vendaLista);
        }
        //System.out.println("qtde: " + qtde + "\nPrec: " + prec + "\ntotal: " + total);        
        venda.setValorVenda(total);
    }
    
    private void removeItem() {
        DefaultTableModel modeloProduto = (DefaultTableModel) tbProdutos.getModel();
        
        String cod = txtRemoveItem.getText().toString();
        for(int x = tbProdutos.getRowCount()-1; x >= 0; x--){
            String coluna = String.valueOf(tbProdutos.getValueAt(x, 0));
            if (coluna.equals(cod)) {
                //aqui vc pega o restante dos valores da linha
                prec = Double.parseDouble(tbProdutos.getValueAt(x, 3).toString().replace(",", "."));                
                total -= prec;
                modeloProduto.removeRow(x);
                //System.out.println("Valor de x: " + x);
            }
        }
        calculaTotal();
    }

