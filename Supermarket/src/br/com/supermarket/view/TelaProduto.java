    public void validaCampos() {
        if ((txtCodigoBarra.getText().trim().isEmpty()) || (txtCodigoBarra.getText().length() != 13)) {
            JOptionPane.showMessageDialog(null, "Informe o Código de Barras corretamente", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtCodigoBarra.requestFocus();
        } else if (txtNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o Nome", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtNome.requestFocus();
        } else if (txtMarca.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe a Marca", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtMarca.requestFocus();
        } else if (txtQuantidade.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe a Quantidade", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtQuantidade.requestFocus();
        } else if (txtQtdMinimaEstoque.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe a Marca", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtQtdMinimaEstoque.requestFocus();
        } else if (txtValorCompra.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe a Marca", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtValorCompra.requestFocus();
        } else if (txtValorVenda.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe a Marca", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtValorVenda.requestFocus();
        } else {
            Produto produto = new Produto();
            ProdutoDAO produtoDAO = new ProdutoDAO();

            produto.setNome(txtNome.getText());
            produto.setCodigoBarra(txtCodigoBarra.getText());
            produto.setMarca(txtMarca.getText());
            produto.setCategoria((CategoriaProduto) cbCategoria.getSelectedItem());
            produto.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            produto.setQuantidadeMinimaEstoque(Integer.parseInt(txtQtdMinimaEstoque.getText()));
            produto.setValorCompra(Double.valueOf(txtValorCompra.getText().toString().replace(",", ".")));
            produto.setValorVenda(Double.valueOf(txtValorVenda.getText().toString().replace(",", ".")));
            produto.setLucro(Double.valueOf(txtLucro.getText().toString().replace(",", ".")));
            produto.setValorTotalEstoque(Double.parseDouble(txtValorTotalEstoque.getText()));
            produto.setValorTotalVenda(Double.parseDouble(txtValorTotalVenda.getText()));

            if (txtId.getText().equals("")) {
                produtoDAO.create(produto);
            } else {
                produto.setId((int)tbProduto.getValueAt(tbProduto.getSelectedRow(), 0));
                produtoDAO.update(produto);
            }

            readJTable();
            limparCampos();
        }
    }
    
    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (tbProduto.getSelectedRow() != -1) {
            
            /* Pré configura a mensagem do ConfirmDialog */
            String alertaNome = String.valueOf(tbProduto.getValueAt(tbProduto.getSelectedRow(), 1));
            String alerta = "Deseja prosseguir com a exclusão do " + alertaNome + "?";
            
            if (JOptionPane.showConfirmDialog(this, alerta, "ATENÇÃO!!!", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                Produto produto = new Produto();
                ProdutoDAO produtoDAO = new ProdutoDAO();

                produto.setId((int)tbProduto.getValueAt(tbProduto.getSelectedRow(), 0));

                produtoDAO.delete(produto);
                readJTable();
                limparCampos();
            } else {
                System.out.println("opção 2");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione na lista abaixo", "Escolha uma opção!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btExcluirActionPerformed

