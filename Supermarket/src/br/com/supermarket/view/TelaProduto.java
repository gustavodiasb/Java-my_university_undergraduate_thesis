    private void eventoTabela() {
        if (tbProduto.getSelectedRow() != -1) {
            Produto produto = new Produto();
            
            txtId.setText(tbProduto.getValueAt(tbProduto.getSelectedRow(), 0).toString());
            txtNome.setText(tbProduto.getValueAt(tbProduto.getSelectedRow(), 1).toString());
            
            /* BUSCAR CAMPOS NO BANCO */
            int id = (int) tbProduto.getValueAt(tbProduto.getSelectedRow(), 0);
            Connection connection = ConexaoDB.getConnection();
            PreparedStatement stmt = null;
            ResultSet rs = null;
            try {
            stmt = connection.prepareStatement("SELECT codigoBarra_produto, marca_produto, qtde_produto, qtdeMin_produto,"
                    + "vlCompra_produto, vlVenda_produto, lucro_produto, vlTotalEstoque_produto, vlTotalVenda_produto, "
                    + "id_categoria, nome_categoria FROM view_produto WHERE id_produto ='" + id + "'");
            rs = stmt.executeQuery();
            rs.first();
            //cbCategoria.removeAllItems();
            CategoriaProduto categoriaProduto = new CategoriaProduto();
                txtCodigoBarra.setText(rs.getString(1));
                txtMarca.setText(rs.getString(2));
                /* --- Double --- */
                txtQuantidade.setText(String.valueOf(rs.getInt(3)));
                txtQtdMinimaEstoque.setText(String.valueOf(rs.getInt(4)));
                txtValorCompra.setText(String.valueOf(rs.getDouble(5)));
                txtValorVenda.setText(String.valueOf(rs.getDouble(6)));
                txtLucro.setText(String.valueOf(rs.getDouble(7)));
                txtValorTotalEstoque.setText(String.valueOf(rs.getDouble(8)));
                txtValorTotalVenda.setText(String.valueOf(rs.getDouble(9)));
                
                categoriaProduto.setId(rs.getInt(10));
                categoriaProduto.setNome(rs.getString(11));
                produto.setCategoria(categoriaProduto);
                
                
                cbCategoria.addItem(produto.getCategoria());
                cbCategoria.setSelectedItem(produto.getCategoria());
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro ao preencher formulário... " + e, "Erro", JOptionPane.ERROR_MESSAGE);
            } finally {
                ConexaoDB.closeConnection(connection, stmt, rs);
            }
        }
    }
    
    
    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tbProduto.getModel();
        modelo.setNumRows(0);
        ProdutoDAO produtoDAO = new ProdutoDAO();
        
        for (Produto produto : produtoDAO.read()) {            
            modelo.addRow(new Object[]{
                produto.getId(),
                produto.getNome(),
            });
        }
    }
    
    public void readJTableForDesk(String desc) {
        DefaultTableModel modelo = (DefaultTableModel) tbProduto.getModel();
        modelo.setNumRows(0);
        ProdutoDAO produtoDAO = new ProdutoDAO();
        
        for (Produto produto : produtoDAO.readForDesc(desc)) {            
            modelo.addRow(new Object[]{
                produto.getId(),
                produto.getNome(),
            });
        }
    }
    
