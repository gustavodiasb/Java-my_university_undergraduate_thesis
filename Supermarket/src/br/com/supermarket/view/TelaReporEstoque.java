    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (txtQuantidade.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe a Quantidade", "Atenção!", JOptionPane.WARNING_MESSAGE);
            txtQuantidade.requestFocus();
        } else {
            Produto produto = new Produto();
            FornecedorDAO fornecedorDAO = new FornecedorDAO();

            produto.setId(IDProd);
            produto.setNome(nomeProd);
            produto.setQuantidadeMinimaEstoque(qtdMinEstoque);
            produto.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            produto.setFornecedor((Fornecedor) cbFornecedor.getSelectedItem());

            fornecedorDAO.reporProduto(produto);
            this.dispose();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

