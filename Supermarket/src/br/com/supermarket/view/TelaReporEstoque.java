    public void reporEstoque(Produto produtoEstoque) {
        IDProd = produtoEstoque.getId();
        nomeProd = produtoEstoque.getNome();
        qtdEstoque = produtoEstoque.getQuantidade();
        qtdMinEstoque = produtoEstoque.getQuantidadeMinimaEstoque();
        limparCampos();
    }
    
