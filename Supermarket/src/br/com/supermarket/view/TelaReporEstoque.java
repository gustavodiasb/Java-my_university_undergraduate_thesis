    public void limparCampos() {
        txtId.setText(String.valueOf(IDProd));
        txtNome.setText(nomeProd);
        txtQuantidade.setText("");
        txtQtdMinimaEstoque.setText(String.valueOf(qtdMinEstoque));
        populaCbFornecedor();
    }
    
    public void populaCbFornecedor() {
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        
        for(Fornecedor fornecedor: fornecedorDAO.read()) {
            cbFornecedor.addItem(fornecedor);
        }
    }

